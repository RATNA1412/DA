s = "Python Programming"
print(s) #print string
print(len(s)) #print string's length
print(s.capitalize()) #first letter capital
print(s.casefold())  #all letter small
print(s.center(30,"*")) #center the string in a field of 30 space
print(s.endswith("ng")) #check if the string ends with "ng"
print(s.count("P")) #counts the occurrences of "p"
print(s.find("P",2)) #find the index of"p" starting feom index 2
print(s.index("y")) #finds the index of "y"
print(s.lower()) #convert to lowercase
print(s.upper()) #convert to uooercase
print(s.isalnum()) #check if the string is alphanumeric
print(s.isalpha()) #check if the string is alphabetic
print(s.replace("P","A")) #replace "p" with "a"
print(s.swapcase()) #swape the case of each character
print(s.title()) #capitalizes the first letter of each word
      

      